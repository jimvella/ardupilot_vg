<h1>About this app</h1>
<p>
  VG diagrams are typically used to help explain aicraft dynamics for upset and
  loss of control training. I was asked if it might be possible to produce a
  data driven VG diagram. Along the way I discoverd drone autopilots. This is
  the result.
</p>
